        <div class="content">
            <p><strong>Gereja Kristen Sumba</strong> atau yang biasa disingkat <strong>GKS</strong>, lahir dan berdiri sendiri pada tanggal <strong>15 Januari 1947</strong>, yang merupakan hasil pekabaran Injil dari <em>Zending Gereformeed Kerken in Nederland (GKN)</em> sejak tahun 1881.</p>

            <h4>Sejarah Pekabaran Injil di Sumba</h4>
            <p>Sejarah pekabaran Injil di Sumba dibagi menjadi <strong>4 Periode</strong>:</p>
            <ul>
                <li><strong>1870an-1881:</strong> Periode awal masyarakat Sumba bersentuhan dengan kekristenan.</li>
                <li><strong>1881-1902:</strong> Periode Perintis.</li>
                <li><strong>1902-1947:</strong> Periode Peletakan Dasar.</li>
                <li><strong>1947-sampai sekarang:</strong> Periode berdiri sendiri.</li>
            </ul>

            <h4>Dinamika Pelayanan GKS</h4>
            <p>Sejak berdiri sendiri, GKS mengalami dinamika pelayanan dalam beberapa periode waktu:</p>
            <ol>
                <li><strong>1947 – 1972:</strong> GKS dalam periode mencari bentuk.</li>
                <li><strong>1970an:</strong> Menyusun rencana lima tahun (RELITA) sebagai Rencana Pendewasaan GKS.</li>
                <li><strong>1980an:</strong> Kesadaran jemaat GKS untuk terlibat aktif dalam pekabaran Injil (PI).</li>
                <li><strong>1990an:</strong> Masa berbenah diri dan mematangkan diri sebagai gereja mandiri, termasuk:
                    <ul>
                        <li>Penjadwalan Sidang Sinode setiap 4 tahun.</li>
                        <li>Kerja sama dengan UKSW dan penyusunan Rencana Induk Pengembangan (1990-1992).</li>
                        <li>Penetapan Tager GKS dalam Sidang Sinode di Ombarade tahun 1998.</li>
                    </ul>
                </li>
                <li><strong>2002:</strong> Penyusunan konsep GBKU dan amandemen Tager GKS.</li>
                <li><strong>2006 – sekarang:</strong> Revitalisasi asas Gerejawi berdasarkan sistem "Presbiterial–Sinodal".</li>
            </ol>

            <h4>Sejarah Kristen di Pulau Sumba</h4>
            <p>Pulau Sumba, yang dahulu dikenal sebagai tanah dengan peperangan, perampokan, perdagangan budak, dan praktik poligami, kini telah menjadi ladang Injil yang subur.</p>

            <h5>Tantangan Penyebaran Injil</h5>
            <p>Pada pertengahan abad ke-19, Pulau Sumba masih terisolasi dan mayoritas penduduknya menganut kepercayaan <strong>Marapu</strong>. Struktur sosial yang terdiri dari kaum pembesar, kaum merdeka, dan kaum budak menjadi penghalang utama bagi Injil.</p>
            <p>Salah satu strategi awal adalah program <strong>transmigrasi orang-orang Kristen dari Pulau Sabu ke Sumba</strong>, yang diharapkan menjadi komunitas percontohan.</p>

            <h5>Awal Pekabaran Injil</h5>
            <p>Pada tahun <strong>1907</strong>, pos zending pertama untuk masyarakat asli Sumba resmi dibuka. Pendidikan menjadi sarana utama dalam pemberitaan Injil. Meskipun menghadapi perlawanan, pada tahun <strong>1915</strong>, beberapa orang asli Sumba akhirnya dibaptis.</p>
            <p>Perkembangan gereja berlanjut hingga tahun <strong>1947</strong>, menandai berdirinya Sinode Gereja Kristen Sumba (GKS).</p>

            <h4>10 Jemaat Pertama Gereja Kristen Sumba</h4>
            <p>Sampai tahun <strong>1947</strong>, terdapat <strong>10 jemaat mandiri</strong> sebelum Sidang Sinode pertama:</p>
            <ol>
                <li>Jemaat Kambaniru (5 Juli 1916)</li>
                <li>Jemaat Melolo (14 September 1919)</li>
                <li>Jemaat Payeti (16 Juli 1930)</li>
                <li>Jemaat Waingapu (5 April 1931)</li>
                <li>Jemaat Patawang (27 Maret 1932)</li>
                <li>Jemaat Rara (7 Desember 1937)</li>
                <li>Jemaat Waikabubak (27 Desember 1937)</li>
                <li>Jemaat Tanggoba (30 November 1937)</li>
                <li>Jemaat Kananggaru (6 April 1938)</li>
                <li>Jemaat Wanukaka (1 Januari 1947)</li>
            </ol>

            <h4>Transformasi Organisasi GKS</h4>
            <p>Sidang Sinode XXX tahun <strong>1980</strong> mulai mengubah struktur organisasi, termasuk mengganti <strong>Kedeputatan</strong> dengan <strong>Bamusdep</strong>. Pada Sidang Sinode XXXIX tahun <strong>2006</strong>, kedeputatan digantikan oleh bidang dan komisi, sementara Bamusdep digantikan oleh <strong>BPMS</strong>.</p>

            <h4>Masuknya GKS ke Sumba Barat Daya</h4>
            <p>Sejarah GKS di Sumba Barat Daya berawal dari kunjungan Ds. de Bruijn dan Ds. Krijger pada tahun <strong>1913</strong>, yang menetapkan pos zending di Tanggo’o dekat Karuni. Sekolah-sekolah dibuka di banyak tempat, termasuk oleh A. Patty dan Guru Luvinus Matatula.</p>
